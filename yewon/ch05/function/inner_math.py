# abs()
print(abs(1))
print(abs(-1))
print(abs(-6.3))
print(abs(6.3))

# divmod()
# a를 b로 나눈 몫과 나머지를 튜플로 리턴
print(divmod(7, 3)) # (2, 1)
print(divmod(8, 2)) # (4, 0)
print(divmod(1, 0.2))
print(divmod(1, 0.25))

# hex()
# 10진수를 16진수 값으로 변환
print(hex(255)) # 0xff
print(hex(160)) # 0xa0
print(hex(3)) # 0x3

# pow()
print(pow(2, 10))
print(pow(5, 3))